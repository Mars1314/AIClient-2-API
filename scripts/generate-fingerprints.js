#!/usr/bin/env node

/**
 * Kiro 账号指纹生成工具
 * 为每个账号生成独立的设备指纹配置
 */

import crypto from 'crypto';

// 浏览器配置模板
const BROWSER_PROFILES = {
    'windows-chrome-120': {
        PLATFORM_NAME: 'Windows',
        BROWSER_VERSION: '120',
        ACCEPT_ENCODING: 'gzip, deflate, br',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9'
    },
    'windows-chrome-121': {
        PLATFORM_NAME: 'Windows',
        BROWSER_VERSION: '121',
        ACCEPT_ENCODING: 'gzip, deflate, br',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9,zh-CN;q=0.8'
    },
    'macos-chrome-120': {
        PLATFORM_NAME: 'macOS',
        BROWSER_VERSION: '120',
        ACCEPT_ENCODING: 'gzip, deflate, br, zstd',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9'
    },
    'macos-chrome-121': {
        PLATFORM_NAME: 'macOS',
        BROWSER_VERSION: '121',
        ACCEPT_ENCODING: 'gzip, deflate, br, zstd',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9,zh-CN;q=0.8,ja;q=0.7'
    },
    'linux-chrome-119': {
        PLATFORM_NAME: 'Linux',
        BROWSER_VERSION: '119',
        ACCEPT_ENCODING: 'gzip, deflate',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9'
    },
    'linux-chrome-120': {
        PLATFORM_NAME: 'Linux',
        BROWSER_VERSION: '120',
        ACCEPT_ENCODING: 'gzip, deflate, br',
        ACCEPT_LANGUAGE: 'en-US,en;q=0.9,es;q=0.8'
    }
};

// 请求间隔配置（毫秒）
const INTERVAL_PROFILES = [
    { min: 1000, max: 3000 },  // 快速
    { min: 1500, max: 4000 },  // 中等
    { min: 2000, max: 5000 },  // 保守
    { min: 1200, max: 3500 },  // 变化1
    { min: 1800, max: 4500 },  // 变化2
];

/**
 * 生成随机盐值
 */
function generateSalt(accountId) {
    const timestamp = Date.now();
    const random = crypto.randomBytes(16).toString('hex');
    return `account-${accountId}-${timestamp}-${random}`;
}

/**
 * 生成 Machine ID
 */
function generateMachineId() {
    return crypto.randomBytes(32).toString('hex');
}

/**
 * 随机选择浏览器配置
 */
function getRandomBrowserProfile() {
    const profiles = Object.keys(BROWSER_PROFILES);
    const randomProfile = profiles[Math.floor(Math.random() * profiles.length)];
    return { profile: randomProfile, ...BROWSER_PROFILES[randomProfile] };
}

/**
 * 随机选择请求间隔配置
 */
function getRandomIntervalProfile() {
    return INTERVAL_PROFILES[Math.floor(Math.random() * INTERVAL_PROFILES.length)];
}

/**
 * 为单个账号生成完整的指纹配置
 */
function generateAccountFingerprint(accountIndex, credFilePath) {
    const browser = getRandomBrowserProfile();
    const interval = getRandomIntervalProfile();
    
    return {
        uuid: `account-${accountIndex}`,
        type: 'claude-kiro-oauth',
        KIRO_OAUTH_CREDS_FILE_PATH: credFilePath,
        
        // 指纹配置
        MACHINE_ID_SALT: generateSalt(accountIndex),
        CUSTOM_MACHINE_ID: '', // 可选：如果需要完全自定义，可以用 generateMachineId()
        
        // 浏览器特征
        BROWSER_VERSION: browser.BROWSER_VERSION,
        PLATFORM_NAME: browser.PLATFORM_NAME,
        ACCEPT_ENCODING: browser.ACCEPT_ENCODING,
        ACCEPT_LANGUAGE: browser.ACCEPT_LANGUAGE,
        
        // 行为特征
        ENABLE_RANDOM_DELAY: true,
        MIN_REQUEST_INTERVAL: interval.min,
        MAX_REQUEST_INTERVAL: interval.max,
        
        // 健康状态
        isHealthy: true,
        isDisabled: false,
        errorCount: 0,
        usageCount: 0,
        lastUsed: null,
        lastErrorTime: null,
        lastErrorMessage: null,
        checkHealth: true,
        checkModelName: 'claude-haiku-4-5',
        
        // 注释信息
        _generated_at: new Date().toISOString(),
        _browser_profile: browser.profile
    };
}

/**
 * 批量生成多个账号的指纹配置
 */
function generateMultipleFingerprints(credFilePaths) {
    const configs = [];
    
    credFilePaths.forEach((filePath, index) => {
        configs.push(generateAccountFingerprint(index + 1, filePath));
    });
    
    return {
        'claude-kiro-oauth': configs,
        _generated_at: new Date().toISOString(),
        _tool_version: '1.0.0',
        _note: 'Generated by generate-fingerprints.js'
    };
}

/**
 * 主函数
 */
function main() {
    const args = process.argv.slice(2);
    
    if (args.length === 0) {
        console.log('Usage: node generate-fingerprints.js <cred-file-path-1> [cred-file-path-2] [...]');
        console.log('');
        console.log('Example:');
        console.log('  node generate-fingerprints.js \\');
        console.log('    ./configs/kiro/account1.json \\');
        console.log('    ./configs/kiro/account2.json \\');
        console.log('    ./configs/kiro/account3.json');
        console.log('');
        console.log('This will generate a provider_pools.json with unique fingerprints for each account.');
        process.exit(1);
    }
    
    const credFilePaths = args;
    const config = generateMultipleFingerprints(credFilePaths);
    
    console.log(JSON.stringify(config, null, 2));
    console.log('');
    console.log(`✅ Generated fingerprints for ${credFilePaths.length} accounts`);
    console.log('');
    console.log('📋 Summary:');
    config['claude-kiro-oauth'].forEach((account, index) => {
        console.log(`  Account ${index + 1}:`);
        console.log(`    - Browser: ${account._browser_profile}`);
        console.log(`    - Platform: ${account.PLATFORM_NAME}`);
        console.log(`    - Version: ${account.BROWSER_VERSION}`);
        console.log(`    - Interval: ${account.MIN_REQUEST_INTERVAL}-${account.MAX_REQUEST_INTERVAL}ms`);
        console.log(`    - Salt: ${account.MACHINE_ID_SALT.substring(0, 30)}...`);
        console.log('');
    });
    console.log('💡 Save the output to provider_pools.json');
}

// 如果直接运行脚本
if (import.meta.url === `file://${process.argv[1]}`) {
    main();
}

export {
    generateAccountFingerprint,
    generateMultipleFingerprints,
    generateSalt,
    generateMachineId
};
